import ProductPage from '../pages/productPage'

Cypress.Commands.add('addFirstProductToCart', () => {
    return cy.get('.card-title a').first().then((product) => {
        const productName = product.text().trim()
        cy.wrap(product).click()
        new ProductPage().addToCart()
        return cy.wrap(productName)
    })
})